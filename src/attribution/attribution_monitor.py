    def acknowledge_alert(self, alert_id: str, user_id: str = None):
        """Acknowledge a security alert."""

        if alert_id in self.active_alerts:
            alert = self.active_alerts[alert_id]
            alert.acknowledged = True
            alert.actions_taken.append(f"Acknowledged by {user_id or 'system'}")

            audit_emitter.emit_security_event(
                "security_alert_acknowledged",
                {
                    "alert_id": alert_id,
                    "acknowledged_by": user_id,
                    "timestamp": datetime.now(timezone.utc).isoformat(),
                },
            )

    def resolve_alert(
        self, alert_id: str, resolution_notes: str = "", user_id: str = None
    ):
        """Resolve a security alert."""

        if alert_id in self.active_alerts:
            alert = self.active_alerts[alert_id]
            alert.resolved = True
            alert.actions_taken.append(
                f"Resolved by {user_id or 'system'}: {resolution_notes}"
            )

            # Move to history only
            del self.active_alerts[alert_id]

            audit_emitter.emit_security_event(
                "security_alert_resolved",
                {
                    "alert_id": alert_id,
                    "resolved_by": user_id,
                    "resolution_notes": resolution_notes,
                    "timestamp": datetime.now(timezone.utc).isoformat(),
                },
            )

    def get_active_alerts(
        self, severity_filter: Optional[AlertSeverity] = None
    ) -> List[SecurityAlert]:
        """Get active security alerts."""

        alerts = list(self.active_alerts.values())

        if severity_filter:
            alerts = [a for a in alerts if a.severity == severity_filter]

        # Sort by severity and timestamp
        severity_order = {
            AlertSeverity.CRITICAL: 5,
            AlertSeverity.HIGH: 4,
            AlertSeverity.MEDIUM: 3,
            AlertSeverity.LOW: 2,
            AlertSeverity.INFO: 1,
        }

        alerts.sort(
            key=lambda a: (severity_order[a.severity], a.timestamp), reverse=True
        )
        return alerts


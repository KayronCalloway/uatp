    def _determine_threat_type(
        self, rule: MonitoringRule, event: AttributionEvent
    ) -> ThreatType:
        """Determine threat type based on rule and event."""

        if "gaming" in rule.rule_id.lower():
            return ThreatType.ATTRIBUTION_GAMING
        elif "sybil" in rule.rule_id.lower():
            return ThreatType.SYBIL_ATTACK
        elif "volume" in rule.rule_id.lower():
            return ThreatType.VOLUME_ATTACK
        elif "confidence" in rule.rule_id.lower():
            return ThreatType.CONFIDENCE_INFLATION
        elif "similarity" in rule.rule_id.lower():
            return ThreatType.SIMILARITY_SPOOFING
        elif event.gaming_detected:
            return ThreatType.ATTRIBUTION_GAMING
        else:
            return ThreatType.TEMPORAL_ANOMALY

    def _calculate_alert_confidence(
        self, rule: MonitoringRule, event: AttributionEvent
    ) -> float:
        """Calculate confidence score for alert."""

        confidence = 0.5  # Base confidence

        # Increase based on anomaly score
        confidence += event.anomaly_score * 0.3

        # Increase based on gaming detection
        if event.gaming_detected:
            confidence += 0.2

        # Increase based on number of risk factors
        confidence += len(event.risk_factors) * 0.1

        # Adjust based on rule historical accuracy
        if rule.rule_triggers > 0:
            accuracy = 1.0 - (rule.false_positives / rule.rule_triggers)
            confidence *= accuracy

        return min(1.0, confidence)

    def _auto_block_entities(self, entity_ids: List[str]):
        """Automatically block entities."""

        for entity_id in entity_ids:
            # This would integrate with the actual blocking system
            audit_emitter.emit_security_event(
                "entity_auto_blocked",
                {
                    "entity_id": entity_id,
                    "reason": "automatic_threat_response",
                    "timestamp": datetime.now(timezone.utc).isoformat(),
                },
            )

        logger.warning(f"Auto-blocked entities: {entity_ids}")

    def _auto_flag_attribution(self, event: AttributionEvent):
        """Automatically flag attribution for review."""

        audit_emitter.emit_security_event(
            "attribution_auto_flagged",
            {
                "event_id": event.event_id,
                "source_entity": event.source_entity,
                "target_entity": event.target_entity,
                "reason": "automatic_threat_response",
            },
        )

    def _generate_coordination_alert(
        self, events: List[AttributionEvent], entities: Set[str]
    ):
        """Generate alert for coordinated activity."""

        alert_id = f"coord_alert_{uuid.uuid4()}"

        alert = SecurityAlert(
            alert_id=alert_id,
            severity=AlertSeverity.HIGH,
            threat_type=ThreatType.COORDINATED_MANIPULATION,
            title="Coordinated Attribution Activity Detected",
            description=f"Detected {len(events)} coordinated attribution events involving {len(entities)} entities",
            affected_entities=list(entities),
            evidence={
                "event_count": len(events),
                "entity_count": len(entities),
                "time_window": "1 minute",
                "event_ids": [e.event_id for e in events],
            },
            confidence=0.8,
        )

        self.active_alerts[alert_id] = alert
        self.alert_history.append(alert)
        self.metrics["coordinated_attacks_detected"] += 1

    def _generate_gaming_alert(
        self, gaming_type: str, events: List[AttributionEvent], threshold: float
    ):
        """Generate alert for gaming activity."""

        alert_id = f"gaming_alert_{uuid.uuid4()}"

        alert = SecurityAlert(
            alert_id=alert_id,
            severity=AlertSeverity.MEDIUM,
            threat_type=ThreatType.ATTRIBUTION_GAMING,
            title=f"Attribution Gaming Detected: {gaming_type}",
            description=f"Detected {gaming_type} gaming pattern targeting threshold {threshold}",
            affected_entities=list(
                set(
                    [e.source_entity for e in events]
                    + [e.target_entity for e in events]
                )
            ),
            evidence={
                "gaming_type": gaming_type,
                "threshold_targeted": threshold,
                "event_count": len(events),
                "event_ids": [e.event_id for e in events],
            },
            confidence=0.7,
        )

        self.active_alerts[alert_id] = alert
        self.alert_history.append(alert)
        self.metrics["gaming_attempts_detected"] += 1

    def _update_realtime_stats(self, event: AttributionEvent):
        """Update real-time statistics."""

        current_minute = datetime.now(timezone.utc).replace(second=0, microsecond=0)

        # Update events per minute
        if (
            not self.realtime_stats["events_per_minute"]
            or self.realtime_stats["events_per_minute"][-1][0] != current_minute
        ):
            self.realtime_stats["events_per_minute"].append((current_minute, 1))
        else:
            last_entry = self.realtime_stats["events_per_minute"][-1]
            self.realtime_stats["events_per_minute"][-1] = (
                last_entry[0],
                last_entry[1] + 1,
            )

        # Update entity activity
        self.realtime_stats["top_entities_24h"][event.source_entity] += 1
        self.realtime_stats["top_entities_24h"][event.target_entity] += 1

        # Update gaming detection rate
        recent_gaming_events = sum(
            1 for e in list(self.recent_events)[-1000:] if e.gaming_detected
        )
        self.realtime_stats["gaming_detection_rate"] = recent_gaming_events / min(
            1000, len(self.recent_events)
        )

        # Update average anomaly score
        recent_anomaly_scores = [
            e.anomaly_score for e in list(self.recent_events)[-1000:]
        ]
        if recent_anomaly_scores:
            self.realtime_stats["average_anomaly_score"] = np.mean(
                recent_anomaly_scores
            )

    def _update_threat_statistics(self, events: List[AttributionEvent]):
        """Update threat type statistics."""

        for event in events:
            if event.gaming_detected:
                self.realtime_stats["threat_types_24h"]["gaming"] += 1

            if event.anomaly_score > 0.8:
                self.realtime_stats["threat_types_24h"]["anomaly"] += 1

            if "sybil" in event.risk_factors:
                self.realtime_stats["threat_types_24h"]["sybil"] += 1

